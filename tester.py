import numpy as np

arr = np.array([1, 2, 3])
print("arr =", arr)
print("arr.shape =", arr.shape)

arr = np.array([[1, 2, 3]])
print("\narr =", arr)
print("arr.shape =", arr.shape)

arr = np.array([[1], [2], [3]])
print("\narr =", arr)
print("arr.shape =", arr.shape)

arr = np.array([[1, 2, 3], [4, 5, 6]])
print("\narr =", arr)
print("arr.shape =", arr.shape)